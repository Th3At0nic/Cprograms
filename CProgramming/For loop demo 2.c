// print a a sentence or any number 10 times using for loop

#include<stdio.h>
int main()


{
    int i;
    for (i=1; i<=10; i++) /*here first part is called initialization,
        second part is called condition, and last one is increment*/
        {
             printf("My name is Rahat.\n");
             printf("Im from Bangladesh\n");
        }


    return 0;
}
